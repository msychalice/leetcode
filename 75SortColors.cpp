class Solution {
public:
    void sortColors(vector<int>& nums) {
        // three pointers
        // initiall move with curPos,
        // but stop at the most left 1,
        // move one step rightwards after a switch
        int mostLeft1 = 0;
        // position for the next 2,
        // move one step leftwards after a switch
        int next2 = nums.size() - 1;  //
        // when find a 0, switch with mostLeft1,
        // find a 2, switch with next2,
        // when curPos == next2, the end
        int curPos = 0;

        while (curPos != next2) {
            if (nums[curPos] == 0) {
                swap(nums[curPos], nums[mostLeft1]);
                mostLeft1++;
                curPos++;
            } else if (nums[curPos] == 1) {
                // don't swap, just move curPost only
                curPos++;
            } else if (nums[curPos] == 2) {
                swap(nums[curPos], nums[next2]);
                next2--;
            }
        }

        // there is a case that the last 0 is not swapped
        if (nums[curPos] == 0) {
            swap(nums[curPos], nums[mostLeft1]);
        }
    }
};

