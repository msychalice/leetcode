class Solution {
public:
    vector<vector<string>> partition(string s) {
        vector<vector<string>> ans;
        vector<string> parts;  // current partitions

        auto isPa = [&](int lo, int hi) {
            while (lo < hi) {
                if (s[lo++] != s[hi--]) {
                    return false;
                }
            }
            return true;
        };

        function<void(int)> backtrack = [&](int startIndex) {
            if (startIndex == s.size()) {
                // end of recursion
                ans.push_back(parts);
            }

            for (int cur = startIndex; cur < s.size(); cur++) {
                if (isPa(startIndex, cur)) {
                    parts.push_back(s.substr(startIndex, cur - startIndex + 1));
                    backtrack(cur + 1);
                    parts.pop_back();
                }
            }
        };

        backtrack(0);

        return ans;
    }
};

