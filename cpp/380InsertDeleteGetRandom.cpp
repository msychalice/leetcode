class RandomizedSet {
public:
    /** Initialize your data structure here. */
    RandomizedSet() {}

    /** Inserts a value to the set. Returns true if the set did not already
     * contain the specified element. */
    bool insert(int val) {
        auto it = m_mapValIndex.find(val);
        if (it != m_mapValIndex.end()) {
            return false;
        }

        m_vecVal.push_back(val);
        m_mapValIndex.emplace(val, m_vecVal.size() - 1);
        return true;
    }

    /** Removes a value from the set. Returns true if the set contained the
     * specified element. */
    bool remove(int val) {
        auto it = m_mapValIndex.find(val);
        if (it == m_mapValIndex.end()) {
            return false;
        }

        int index = it->second;
        swap(m_vecVal[index], m_vecVal[m_vecVal.size() - 1]);
        m_mapValIndex[m_vecVal[index]] = index;

        m_vecVal.pop_back();
        m_mapValIndex.erase(it);

        return true;
    }

    /** Get a random element from the set. */
    int getRandom() {
        // get a random number at [0, m_vecVal.size()-1]
        int randomIndex = rand() % m_vecVal.size();

        return m_vecVal[randomIndex];
    }

private:
    unordered_map<int, int> m_mapValIndex;
    vector<int> m_vecVal;
};

