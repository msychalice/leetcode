/**
 * Definition for singly-linked list.
 */
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

void printList(const ListNode* node) {
    while (node != nullptr) {
        cout << node->val;

        node = node->next;
        
        if (node != nullptr) {
            cout << "->";
        } else {
            cout << endl;
        }
    }
}

class Solution {
public:
    double myPow(double x, int n) {
        if (n == 0) {
            return 1;
        }
        if (n == 1) {
            return x;
        }
        if (n == 2) {
            return x * x;
        }
        if (n == -1) {
            return 1 / x;
        }
        if (n == -2) {
            return 1 / (x * x);
        }

        /*
        if (n < 0) {
            return 1 / myPow(x, -n);    // overflow when n = int::min
        }
        */

        if (n % 2 == 0) {  // n is even
            return myPow(myPow(x, n / 2), 2);
        } else {
            return myPow(x, n - 1) * x;
        }
    }
};

