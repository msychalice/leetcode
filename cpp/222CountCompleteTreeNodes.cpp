template<class T>
void printMap(const T& container) {
//    return;

    cout << "size " << container.size() << endl;
    if (container.size() == 0) {
        cout << "[]" << endl;
        return;
    }
    for (auto it = container.begin(); it != container.end(); it++) {
        if (it == container.begin()) {
            cout << "[ ";
        }

        cout << "[" << it->first << ", " << it->second << "]";

        if (next(it) == container.end()) {
            cout << " ]" << endl;
        } else {
            cout << ", ";
        }
    }
}

/**
 * Definition for singly-linked list.
 */
struct ListNode {
    int val;
    ListNode* next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode* next) : val(x), next(next) {}
};

void printList(const ListNode* node) {
    while (node != nullptr) {
        cout << node->val;

        node = node->next;
        
        if (node != nullptr) {
            cout << "->";
        } else {
            cout << endl;
        }
    }
}

/**
 * Definition for a binary tree node.
 */
struct TreeNode {
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode* left, TreeNode* right)
        : val(x), left(left), right(right) {}
};

void printTree(TreeNode* root) {
    if (root == nullptr) {
        return;
    }

    cout << root->val << endl;
    printTree(root->left);
    printTree(root->right);
}

// Definition for a Node.
class Node {
public:
    int val;
    Node* left;
    Node* right;
    Node* next;
    Node() : val(0), left(nullptr), right(nullptr), next(nullptr) {}
    Node(int _val) : val(_val), left(nullptr), right(nullptr), next(nullptr) {}
    Node(int _val, Node* _left, Node* _right, Node* _next)
        : val(_val), left(_left), right(_right), next(_next) {}
};

class Solution {
public:
    int countNodes(TreeNode* root) {
        if (root == nullptr) {
            return 0;
        }

        function<int(TreeNode*)> calcHeight = [](TreeNode* node) {
            if (node == nullptr) {
                return 0;
            }

            int height = 0;

            while (node != nullptr) {
                height++;
                node = node->left;
            }
            return height;
        };

        int height = calcHeight(root);
        int leftHeight = height - 1;

        if (leftHeight == 0) {  // doesnt have left subtree nor right subtree
            return 1;
        }

        int rightHeight = calcHeight(root->right);

        // left subtree is a full binary tree,
        // so we can immediately get the node count of left subtree
        if (leftHeight == rightHeight) {
            int countLeftSubtree = pow(2, leftHeight) - 1;
            return countNodes(root->right) + countLeftSubtree + 1;
        } else {  // right subtree is a full binary tree
            int countRightSubtree = pow(2, rightHeight) - 1;
            return countNodes(root->left) + countRightSubtree + 1;
        }
    }
};

int main() {
    Solution s;

    TreeNode node4(4);
    TreeNode node5(5);
    TreeNode node6(6);
    TreeNode node2(2, &node4, &node5);
    TreeNode node3(3, &node6, nullptr);
    TreeNode node1(1, &node2, &node3);

    auto output = s.countNodes(&node1);

    cout << "*************output*************" << endl;

    cout << output << endl;

    return 0;
}
